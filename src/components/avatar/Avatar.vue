<template>
  <article class="avatar">
    <h2 v-if="withGreetins">{{ getGreetings }}</h2>
    <section class="image">
      <img src="../../assets/images/boy.gif" :alt="`Avatar de ${name}`" />
      <div class="info">
        <span class="aka">{{ level }}</span>
        <p class="points">
          Record:
          <span class="points__value">65</span> puntos.
        </p>
      </div>
    </section>
    <button-custom class="share" simple v-ripple color-secondary
      >Compartir</button-custom
    >
    <slot></slot>
  </article>
</template>
<script>
import * as moment from "moment";

export default {
  name: "Avatar",
  props: {
    name: {
      type: String,
      default: "Aureliano Buendía"
    },
    level: {
      type: String,
      default: ""
    },
    withGreetins: {
      type: Boolean,
      default: true
    }
  },
  computed: {
    getGreetings() {
      return `${this.getGreetingTime(moment())}, ${this.name}.`;
    }
  },
  methods: {
    getGreetingTime: currentTime => {
      if (!currentTime || !currentTime.isValid()) {
        return "Hello";
      }

      const splitAfternoon = 12; // 24hr time to split the afternoon
      const splitEvening = 17; // 24hr time to split the evening
      const currentHour = parseFloat(currentTime.format("HH"));

      if (currentHour >= splitAfternoon && currentHour <= splitEvening) {
        // Between 12 PM and 5PM
        return "Buenas tardes";
      } else if (currentHour >= splitEvening) {
        // Between 5PM and Midnight
        return "Buenas noches";
      }
      // Between dawn and noon
      return "Buenos días";
    }
  }
};
</script>
