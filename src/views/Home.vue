<template>
  <div page>
    <header-nav
      @onToggleCollapse="$emit('onToggleCollapse', $event)"
      :withBack="false"
    ></header-nav>
    <div class="home">
      <h1>Resultado: {{ points }} / {{ totalQuestions }}</h1>
      <wheel
        :forceAngularVelocity="angularVelocity"
        :wasLaunched="wasLaunched"
        @lockLaunch="wasLaunched = true"
      ></wheel>
      <div class="fixed">
        <button-custom
          simple
          big
          color-secondary
          v-ripple
          @click="launch"
          style="position: relative;"
        >
